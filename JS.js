// Создание объекта с методом и ключевым словом this.
var person = {
  firstName: "Brad",
  age: 45,
  children: ["Liza", "Perya"],
  address: {
    street: "555 Some st",
    city: "Boston",
    state: "MA",
  },
  NameAge: function () {
    return this.firstName + ", age is - " + this.age;
  },
};

// Обращение к объекту и методу.
console.log(person);
console.log(person.firstName);
console.log(person.address.city);
console.log(person.NameAge());

// Конструктор создания объектов.
var apple = new Object();
apple.color = "Green";
apple.shape = "Round";
apple.describe = function () {
  return "An apple is " + apple.color + " and " + apple.shape;
};
console.log(apple.color + apple.shape);
console.log(apple.describe());

