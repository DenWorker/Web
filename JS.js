/*ЦИКЛЫ*/
/*
    -Конструкции, которые позволяют многократно повторать действия;
    -Первый параметр - это счётчик, второй - условие выхода, третий действия (++, --, +=, ...);
    -Если 1 строчка кода, то фигурные скобки не нужны.
*/

// Цикл for.
for (var i = 0; i <= 10; i++) {
  console.log(i); // Вывод в консоль значений от 0 до 10 включительно;
}

// Цикл с предусловием.
var i = 0;
while (i <= 10) {
  console.log(i);
  i++;
}

// Цикл с постусловием. Он выполнится хотябы 1 раз.
i = 10;
do {
  console.log(i);
  i -= 2;
} while (i > 0);

// Вывод массива с использолванием цикла.
for (var i = 0; i < colors2.length; i++) {
  console.log(colors2[i]);
}

